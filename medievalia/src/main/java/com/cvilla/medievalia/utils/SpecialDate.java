package com.cvilla.medievalia.utils;

public class SpecialDate {
	private int anio;
	private int mes;
	private int dia;
	
	public SpecialDate() {
		super();
	}
	public int getAnio() {
		return anio;
	}
	public void setAnio(int anio) {
		this.anio = anio;
	}
	public int getMes() {
		return mes;
	}
	public void setMes(int mes) {
		this.mes = mes;
	}
	public int getDia() {
		return dia;
	}
	public void setDia(int dia) {
		this.dia = dia;
	}
	
	

}
